# List
my_list = [1, 2, 3, 4]
my_list.append(5)
print(my_list, my_list[0])

# Tuple (Immutable)
my_tuple = (1, 2, 3)
print(my_tuple[0])

# Set
my_set = {1, 2, 3, 4}
my_set.add(5)
print(my_set)

# Dictionary
my_dict = {"name": "John", "age": 25}
my_dict["city"] = "NY"
print(my_dict["name"], my_dict)
