#include <stdio.h>

int main()
{
    int num = 10;
    float decimal = 3.14;
    char letter = 'A';
    double largeDecimal = 10.3456789;

    printf("Integer: %d\n", num);
    printf("Float: %.2f\n", decimal);
    printf("Character: %c\n", letter);
    printf("Double: %lf\n", largeDecimal);
    return 0;
}
